fn main() {
    let args: Vec<String> = std::env::args().skip(1).collect();
    let text = args.join(" ");
    let length = text.len();
    let padding = text.len() / 2 + 2;
    let bars_length = length + 4;
    print!(
        "{}\n< {text} >\n{}\n{}\\\n{}\\",
        "_".repeat(bars_length),
        "-".repeat(bars_length),
        " ".repeat(padding),
        " ".repeat(padding + 1)
    );
    print!("{}", indent(ASCII, padding));
}

fn indent(s: &str, spaces: usize) -> String {
    s.lines()
        .map(|line| format!("{:>width$}{line}", "", width = spaces))
        .collect::<Vec<_>>()
        .join("\n")
}
